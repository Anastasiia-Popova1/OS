#include <iostream>
#include <cstdlib>
#include <ctime>
#include <iomanip>
using namespace std;

const int MAX_SIZE = 100; 

int main() {
    srand(time(NULL)); 
    
    int matrix[MAX_SIZE][MAX_SIZE];
    int n, m;
    
    cout << "Введіть кількість рядків: ";
    cin >> n;
    cout << "Введіть кількість стовпців: ";
    cin >> m;
    
    cout << "\nСформована матриця:\n";
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < m; j++) {
            matrix[i][j] = rand() % 100 - 50; 
            cout << setw(5) << matrix[i][j] << " ";
        }
        cout << endl;
    }
    int minEven = 0; 
    bool found = false;
    
    int diagSize = (n < m) ? n : m;             //визначення розміру діагоналі 

    cout << "\nЕлементи головної діагоналі: ";
    for (int i = 0; i < diagSize; i++) {
        cout << matrix[i][i] << " ";
    }
    cout << endl;
    
    for (int i = 0; i < diagSize; i++) {        //пошук мін парного числа
        if (matrix[i][i] % 2 == 0) { 
            if (!found) { 
                minEven = matrix[i][i];
                found = true;
            } else if (matrix[i][i] < minEven) {
                minEven = matrix[i][i];
            }
        }
    }
    
    if (found) {
        cout << "Мінімальне парне число головної діагоналі: " << minEven << endl;
    } else {
        cout << "На головній діагоналі немає парних чисел" << endl;
    }
    return 0;
}

