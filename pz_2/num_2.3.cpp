// Далі виконується пошук мінімального парного числа на головній діагоналі матриці. Головна діагональ - номер рядка дорівнює номеру 
// стовпця. Оскільки матриця може бути не квадратною, розмір головної діагоналі обмежується мінімальним з розмірів матриц. Спочатку 
// програма виводить усі елементи головної діагоналі для наочності. Потім за допомогою циклу for виконується перебір усіх елементів 
// діагоналі. Для кожного елемента за допомогою умовного оператора if перевіряється, чи є число парним. Якщо парний елемент знайдено, 
// він порівнюється з поточним мінімальним значенням. Змінна found використовується як прапорець, щоб визначити, чи було знайдено хоча 
// б один парний елемент на діагоналі. Якщо це перший знайдений парний елемент, він стає початковим мінімумом. Якщо знайдено наступний 
// парний елемент, який менший за поточний мінімум, він його замінює. Після завершення циклу перевіряється значення змінної found. Якщо 
// вона дорівнює true, програма виводить знайдене мінімальне парне число.



