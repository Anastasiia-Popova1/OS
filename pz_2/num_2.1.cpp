const int MAX_SIZE = 100; 

void printArray(int arr[], int n, const string& message) {      //вивід масиву
    cout << message << ": ";
    for (int i = 0; i < n; i++) {
        cout << arr[i] << " ";
    }
    cout << endl;
}

void deleteMinElement(int arr[], int& n) {                      //видалення мін елементу
    if (n == 0) return;

    int minIndex = 0;
    for (int i = 1; i < n; i++) {
        if (arr[i] < arr[minIndex]) {
            minIndex = i;
        }
    }

    for (int i = minIndex; i < n - 1; i++) {
        arr[i] = arr[i + 1];
    }
    n--;
}


void addElements(int arr[], int& n, int N, int K) {             //додавання N елементів, починаючи з номера K
    if (N <= 0 || K < 0 || K > n) return;
    

    for (int i = n + N - 1; i >= K + N; i--) {                  //зсув вправо
        arr[i] = arr[i - N];
    }

    for (int i = 0; i < N; i++) {                               //нові елементи
        cout << "Введіть елемент для позиції " << K + i << ": ";
        cin >> arr[K + i];
    }
    n += N;
}

void swapEvenOdd(int arr[], int n) {                            //перестановка парних та непарних номерів
    for (int i = 0; i < n - 1; i += 2) {
        swap(arr[i], arr[i + 1]);
    }
}

int linearSearch(int arr[], int n, int key, int& comparisons) { //лінійний пошук за ключем
    comparisons = 0;
    for (int i = 0; i < n; i++) {
        comparisons++;
        if (arr[i] == key) {
            return i;
        }
    }
    return -1;
}

void bubbleSort(int arr[], int n) {                             //сорт. бульбашкою
    for (int i = 0; i < n - 1; i++) {
        for (int j = 0; j < n - i - 1; j++) {
            if (arr[j] > arr[j + 1]) {
                swap(arr[j], arr[j + 1]);
            }
        }
    }
}

int binarySearch(int arr[], int n, int key, int& comparisons) { //бінарний пошук 
    comparisons = 0;
    int left = 0, right = n - 1;
    while (left <= right) {
        comparisons++;
        int mid = left + (right - left) / 2;
        if (arr[mid] == key) {
            return mid;
        }
        else if (arr[mid] < key) {
            left = mid + 1;
        }
        else {
            right = mid - 1;
        }
    }
    return -1;
}

