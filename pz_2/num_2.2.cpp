// в цьому рішенні числа генеруються від 1 до 6, щоб ми могли відстежити повтори при довжині масиву 7 та більше.

// Далі виконується підрахунок частоти кожного елемента за допомогою вкладеного циклу for. Зовнішній цикл проходить по всіх 
// елементах масиву, а оператор if перевіряє, чи цей елемент ще не був підрахований. Якщо елемент ще не враховано, змінній 
// frequency[i] присвоюється значення 1, після чого внутрішній цикл починає перевіряти всі наступні елементи масиву. При 
// знаходженні рівного елемента лічильник збільшується на 1, а відповідний елемент позначається як counted[j] = true, 
// щоб уникнути повторного підрахунку. Таким чином, для кожної унікальної групи елементів визначається, скільки разів 
// вона зустрічається в масиві.

// Далі утворюється нова послідовність. Цикл for проходить по всіх елементах вихідного масиву. Умовний оператор if перевіряє, чи 
// елемент є першим у своїй групі та чи зустрічається він лише один раз. Якщо умова виконується, елемент додається до нового масиву
// newArr, а лічильник newN збільшується. Так ми видаляємо всі елементи, які зустрічаються більше одного разу.



