#include <iostream>
#include <cstdlib>
#include <ctime>
using namespace std;

const int MAX_SIZE = 100;

int main() {
    srand(time(NULL));
    int arr[MAX_SIZE]; 
    int n; 
    
    cout << "Введіть розмір масиву: ";
    cin >> n;
    cout << "Згенерований масив: ";
    for (int i = 0; i < n; i++) { 
        arr[i] = rand() % 6 + 1;
        cout << arr[i] << " ";
    }
    cout << endl;
    
    int frequency[MAX_SIZE] = {0};
    bool counted[MAX_SIZE] = {false};
    
    for (int i = 0; i < n; i++) {                   //підрахунок частоти кожного елемента
        if (!counted[i]) {
            frequency[i] = 1;
            for (int j = i + 1; j < n; j++) {
                if (arr[i] == arr[j]) {
                    frequency[i]++;
                    counted[j] = true; 
                }
            }
        }
    }

    int newArr[MAX_SIZE];           
    int newN = 0;
    
    for (int i = 0; i < n; i++) {                   //утворення нової послідовності
        if (!counted[i] && frequency[i] == 1) {
            newArr[newN] = arr[i];
            newN++;
        }
    }
    
    cout << "Нова послідовність: ";
    for (int i = 0; i < newN; i++) {
        cout << newArr[i] << " ";
    }
    return 0;
}

